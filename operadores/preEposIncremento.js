/* 
 * Pré e Pós-Incremento
 * Pré-incremento e pós-incremento são operadores que aumentam o valor de uma variável em 1.
 */

let n= 10;

console.log(n++); // Pré-incremento: incrementa n antes de usá-lo, resultado é 11
console.log(n);   // Valor atual de n após o pré-incremento: 11
console.log(++n); // Pós-incremento: incrementa n antes de usá-lo, resultado é 12
console.log(n);   // Valor atual de n após o pós-incremento: 12
console.log(n--); // Pós-decremento: decrementa n antes de usá-lo, resultado é 12
console.log(n);   // Valor atual de n após o pós-decremento: 11
console.log(--n); // Pré-decremento: decrementa n antes de usá-lo, resultado é 10
console.log(n);   // Valor atual de n após o pré-decremento: 10